#!/usr/bin/env node
/**
 * Supabase TypeScript type generator.
 *
 * Generates src/types/database.types.ts from the linked Supabase project schema.
 * Prepends a stable header so the file declares its provenance and how to
 * regenerate it.
 *
 * Modes:
 *   node scripts/gen-supabase-types.mjs           # write file
 *   node scripts/gen-supabase-types.mjs --check   # exit 1 if file is stale
 *
 * Auth:
 *   Requires `supabase login` + `supabase link` to have been run, OR
 *   set SUPABASE_PROJECT_ID env var to bypass --linked and target a project ref.
 *
 * Why a script and not a one-line `supabase gen types ... > file`?
 *   - Need to prepend a "DO NOT EDIT" header.
 *   - Need a --check mode for CI that compares regenerated output to the
 *     committed file without polluting it.
 *   - Cross-platform (Windows vs Unix) shell-redirect quoting is fragile.
 */

import { spawnSync } from 'node:child_process';
import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const repoRoot = join(__dirname, '..');
const targetFile = join(repoRoot, 'src', 'types', 'database.types.ts');

const HEADER = `// ============================================================================
// Auto-generated by \`supabase gen types typescript\`. DO NOT EDIT BY HAND.
//
// To regenerate after a migration:
//   npm run supabase:gen-types
//
// CI verifies this file is in sync via \`npm run supabase:check-types\`
// (advisory now, blocking once stable). See docs/architecture/db-type-generation.md.
//
// First-time generation must run in an environment with the Supabase CLI
// authenticated and access to the linked project. After that, this file is
// committed and CI verifies it on every PR.
// ============================================================================

`;

const isCheck = process.argv.includes('--check');

const projectId = process.env.SUPABASE_PROJECT_ID;
const args = ['gen', 'types', 'typescript'];
if (projectId) {
  args.push('--project-id', projectId);
} else {
  args.push('--linked');
}

const result = spawnSync('supabase', args, { encoding: 'utf-8' });

if (result.error) {
  console.error('Failed to spawn `supabase` CLI:', result.error.message);
  console.error('Install: https://supabase.com/docs/guides/cli');
  process.exit(2);
}

if (result.status !== 0) {
  console.error('`supabase gen types typescript` failed:');
  console.error(result.stderr || result.stdout);
  if (!projectId) {
    console.error('\nHint: ensure `supabase login` + `supabase link` were run, or set SUPABASE_PROJECT_ID.');
  }
  process.exit(result.status ?? 1);
}

const generated = HEADER + result.stdout;

if (isCheck) {
  if (!existsSync(targetFile)) {
    console.error(`Missing committed types file: ${targetFile}`);
    console.error('Run `npm run supabase:gen-types` and commit the result.');
    process.exit(1);
  }
  const current = readFileSync(targetFile, 'utf-8');
  // Normalize line endings so a Windows-checked-out file with CRLF
  // doesn't always diverge from CLI stdout (LF).
  const norm = (s) => s.replace(/\r\n/g, '\n');
  if (norm(current) === norm(generated)) {
    console.log('Supabase types are up to date.');
    process.exit(0);
  }
  console.error('Supabase types are out of sync with migrations.');
  console.error('Run `npm run supabase:gen-types` locally and commit the result.');
  process.exit(1);
}

writeFileSync(targetFile, generated, 'utf-8');
console.log(`Wrote ${targetFile}`);
